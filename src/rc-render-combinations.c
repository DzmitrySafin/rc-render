gboolean
add_f2l_data (GArray* combinations) {
  gint32 c, i, j;

  for (c = 0; c < G_N_ELEMENTS (DATA_F2L); c++) {
    for (j = 0; j < 3; j++) {
      for (i = 0; i < CUBE_NUMBER; i++) {
        if (!parse_f2l_line (DATA_F2L[c].combinations.text[j][i], &DATA_F2L[c].combinations.data[CUBE_NUMBER * CUBE_NUMBER * j + CUBE_NUMBER * i])) {
          return FALSE;
        }
      }
    }
  }

  g_array_append_vals (combinations, DATA_F2L, G_N_ELEMENTS (DATA_F2L));
  return TRUE;
}

