gboolean
parse_f2l_line (const gchar* line, guchar data[])
{
  gint i, n;
  gboolean end;

  if (line == NULL || line[0] == '\x0') {
    return FALSE;
  }

  end = FALSE;
  for (i = 0, n = 0; n < CUBE_NUMBER; i++) {
    switch (line[i]) {
      case '\x0':
        end = TRUE;
        break;
      case 'T':
      case 't':
        data[n++] = CUBE_COLOR_TOP;
        break;
      case 'F':
      case 'f':
        data[n++] = CUBE_COLOR_FRONT;
        break;
      case 'R':
      case 'r':
        data[n++] = CUBE_COLOR_RIGHT;
        break;
      case 'D':
      case 'd':
        data[n++] = CUBE_COLOR_DOWN;
        break;
      case '0':
        data[n++] = CUBE_COLOR_NEUTRAL;
        break;
      default:
        /* skip the character */
        break;
    }
    if (end) {
      break;
    }
  }

  return n == CUBE_NUMBER;
}

gboolean
parse_oll_line (const gchar* line, guchar data[])
{
  gint i, n;
  gboolean end;

  if (line == NULL || line[0] == '\x0') {
    return FALSE;
  }

  end = FALSE;
  for (i = 0, n = 0; n < CUBE_NUMBER_EX; i++) {
    switch (line[i]) {
      case '\x0':
        end = TRUE;
        break;
      case 'T':
      case 't':
      case '=':
        data[n++] = CUBE_COLOR_TOP;
        break;
      case '0':
      case '-':
      case 'X':
      case 'x':
        data[n++] = CUBE_COLOR_NEUTRAL;
        break;
      default:
        /* skip the character */
        break;
    }
    if (end) {
      break;
    }
  }

  return n == CUBE_NUMBER_EX;
}

gboolean
parse_pll_line (const gchar* line, guchar data[])
{
  gint i, n;
  gboolean end, num;

  if (line == NULL || line[0] == '\x0') {
    /* not used overlay */
    for (n = 0; n < CUBE_NUMBER; n++) {
      data[n] = 0;
    }
    return TRUE;
  }

  end = FALSE;
  num = FALSE;
  for (i = 0, n = 0; n < CUBE_NUMBER || num; i++) {
    switch (line[i]) {
      case '\x0':
        end = TRUE;
        break;
      case '0':
      case '1':
      case '2':
      case '3':
      case '4':
      case '5':
      case '6':
      case '7':
      case '8':
      case '9':
        if (num) {
          data[n-1] = data[n-1] * 10 + (line[i] - '0');
        } else {
          data[n++] = line[i] - '0';
        }
        num = TRUE;
        break;
      default:
        /* skip the character */
        num = FALSE;
        break;
    }
    if (end) {
      break;
    }
  }

  return n == CUBE_NUMBER;
}

